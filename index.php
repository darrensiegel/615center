                <h2 class="title">About the Home</h2>



                <div class="page-description">
                    <p>Listed on the National Register of Historic Places
                      as part of the Sauer Buildings Historic District, this Craftsman-style Bungalow
                      with stone and cedar exterior presents the buyer a one
                      of a kind opportunity to own a truly unique home.
                      </p>

                    <p>The home, nestled in a wooded hillside just above Aspinwall, has many
                       &quot;fairy-tale&quot; features like unusual stonework,
                        arched doorways, and an overhanging stone balcony. Original features from the
                          home's 1925 construction such as beautiful woodwork, glass paneled doors
                          with glass doorknobs, and ceramic subway tile
                          accent modern conveniences not found in most homes in this area
                          including an open floor plan, central air conditioning, and
                          deep and walk in closets. The fenced in yard is unusually large for Aspinwall
                          as is the integral garage and four car, brick driveway.
                      </p>


                    <p>
                      Renovations of the original floor plan include the
                      creation of a breakfast room from
                      previous 5th bedroom adjacent to kitchen and removal of a wall and the
                      opening of another to join kitchen, dining room and foyer.
                      Recent upgrades include new hardwood floors in first floor,
                      refinished hardwood floors in second floor, new stainless steel kitchen
                      appliances, four skylights, and conversion of fireplace to natural gas.
                      The basement level features a 400+ square foot finished family room complete
                      with stainless steel kitchenette.
                    </p>

                    <p>
                      Ongoing landscaping includes a
                      native and edible landscape plan incorporating fruit trees and a large strawberry patch. </p>

                    <p>Buyers' agents welcome.</p>
